int		ft_atoi(const char *str)
{
	int	r;
	int	j;

	j = 1;
	r = 0;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if (*str == '-')
	{
		j = -1;
		str++;
	}
	else if (*str == '+')
		str++;
	while (*str != '\0' && *str >= 48 && *str <= 57)
	{
		r = r * 10 + (*str - 48);
		str++;
	}
	return (r * j);
}
